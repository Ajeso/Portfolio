window.addEventListener('scroll', function(){

  
  if(window.pageYOffset>50) {
    navbar.classList.add("sticky-red");
  } else {
    navbar.classList.remove("sticky-red");
  }
});


window.addEventListener('scroll', function(){

  
  if(window.pageYOffset>50) {
    navbar.span.add("span");
  } else {
    navbar.span.remove("span");
  }
});